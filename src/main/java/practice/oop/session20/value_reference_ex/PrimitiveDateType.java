package practice.oop.session20.value_reference_ex;

public class PrimitiveDateType {

    //-128 > 127
    byte myByte = 12;

    //16 bit integer value
    //-32768 > 32767
    short myShort = 32;

    //32 bit integer value
    int myInt = 12345;

    //64 bit integer value
    long myLong = 1234567890;

    double myDouble = 12.55;

    //64 bit integer value; about 15 decimal digits
    float myFloat = 6.3F;

    boolean myBoolean = true;

    char myChar = 'a';
}
